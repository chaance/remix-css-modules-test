import type { LinksFunction, MetaFunction } from "@remix-run/node";
import {
  Links,
  LiveReload,
  Meta,
  Outlet,
  Scripts,
  ScrollRestoration,
} from "@remix-run/react";
import { cssModulesStylesheetUrl } from "@remix-run/css-modules";

export const meta: MetaFunction = () => ({
  charset: "utf-8",
  title: "New Remix App",
  viewport: "width=device-width,initial-scale=1",
});

export const links: LinksFunction = () => {
  let mylinks = [];
  console.log({ cssModulesStylesheetUrl });

  if (cssModulesStylesheetUrl) {
    mylinks.push({
      rel: "stylesheet",
      href: cssModulesStylesheetUrl,
    });
  }

  return mylinks;
};

export default function App() {
  return (
    <html lang="en">
      <head>
        <Meta />
        <Links />
      </head>
      <body>
        <Outlet />
        <ScrollRestoration />
        <Scripts />
        <LiveReload />
      </body>
    </html>
  );
}
